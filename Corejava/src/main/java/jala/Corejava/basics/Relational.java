package jala.Corejava.basics;

public class Relational {
	
	public static void main(String[] args) {
		int x=2,y=6;
		System.out.println(" x>y is = " + (x>y)); //here x>y is relational operator
		
		int a=10,b=5;
		int c=3,d=4;
		System.out.println(a>b && c>d);//here && is logical and operator
		
		//boolean bool=true;
		boolean bool=(12>4);
		System.out.println(bool);//here is boolean datatype
		
		char m=65;
		System.out.println(m);
		
	
	}

}
